         <header>
             <p> La balise <code>&lt;title&gt;</code> définit le titre du document (qui est affiché dans la barre de titre du
                 navigateur ou dans l'onglet de la page).
                 Cet élément ne peut contenir que du texte, les balises qu'il contiendrait seraient ignorées.<br />
                Il est toujours utilisé dans l'élément <code>&lt;head&gt;</code> de la page </p> 

            <strong> Exemple :</strong><br />
                <img src="./media/extitle.png" alt="extitle">
