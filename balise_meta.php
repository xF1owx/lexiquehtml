        <h2>
            Présentation de la balise <code>&lt;meta&gt;</code>
        </h2>

        <p>L'élément <code>&lt;meta&gt;</code> représente toute information de métadonnées qui ne peut pas être représentée par un des éléments 
            <code>&lt;base&gt;</code>, <code>&lt;link&gt;</code>, <code>&lt;script&gt;</code>, <code>&lt;style&gt;</code> ou <code>&lt;title&gt;</code>.<br />
            Comme l'élément <code>&lt;img&gt;</code>,<code>&lt;meta&gt;</code> n'a pas besoin de balise de fin.<br />
            Les informations inclues dans l'élément meta sont destinées aux navigateurs web, aux moteurs de recherche et aux outils d'indexation.
        </p><br />
        <p><strong>Exemple :</strong><br/>
            <img src="./media/exmeta.png"/>
        </p>
    
